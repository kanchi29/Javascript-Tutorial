console.log("Operators in js");

//Arithmetic operators
let a= 45;
let b= 4;
console.log("a+b=", a+b);
console.log("a-b=", a-b);
console.log("a*b=", a*b);
console.log("a/b=", a/b);
console.log("a**b=", a**b);
console.log("a%b=", a%b);
console.log("a++=", a++);
console.log("a--=", a--);
console.log("++a=", ++a);

//Assignment Operators
let c = 25;
console.log("c = ", c += 5);
console.log("c = ", c -= 5);
console.log("c = ", c *= 2);
console.log("c = ", c /= 5);
console.log("c = ", c **= 2);